    public void preencherTabela(String SQL){
       
ArrayList<Object> dados = new ArrayList<Object>();
        
        String[] Colunas = new String[]{"ID", "Nome", "Valor"};
        
        conecta.executaSQL(SQL);
        try{
            conecta.rs.first();
            do{
                dados.add(new Object[]{conecta.rs.getInt("id"),conecta.rs.getString("nome"),conecta.rs.getDouble("valor")});
        }while(conecta.rs.next());
        }catch(SQLException ex){
          JOptionPane.showMessageDialog(null, "Erro ao preencher o ArrayList" + ex);
        }
    
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        Tabela.setModel(modelo);
        Tabela.getColumnModel().getColumn(0).setPreferredWidth(80);
        Tabela.getColumnModel().getColumn(0).setResizable(false);
        Tabela.getColumnModel().getColumn(1).setPreferredWidth(180);
        Tabela.getColumnModel().getColumn(1).setResizable(false);
        Tabela.getColumnModel().getColumn(2).setPreferredWidth(80);
        Tabela.getColumnModel().getColumn(2).setResizable(false);
        Tabela.getTableHeader().setReorderingAllowed(false);
        Tabela.setAutoResizeMode(Tabela.AUTO_RESIZE_OFF);
        Tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
