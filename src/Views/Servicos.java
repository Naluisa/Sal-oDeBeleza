    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
        modelo.setNumRows(0);
        
         Connection con = Conexao.getConnection();
        ServicoDAO sdao = new ServicoDAO(con);
        
        for(Servico s: sdao.read()){
            modelo.addRow(new Object[]{
                s.getId(),
                s.getNome(),
                s.getValor()
            });
        }
    }
            
            
